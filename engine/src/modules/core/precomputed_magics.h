#define STATIC_MAGIC_ATTACKS(type, pos, value, bit_size)        \
static bitboard s_##type##_attacks_##pos[1ULL << bit_size];     \
static bitboard s_##type##_value_##pos = value##ULL;            \
static uint8_t s_##type##_shift_quantity_##pos = 64 - bit_size;

#define STATIC_MAGIC_DIAG_ATTACK(pos, value, bit_size) STATIC_MAGIC_ATTACKS(diagonal, pos, value, bit_size)
#define STATIC_MAGIC_ORTHO_ATTACK(pos, value, bit_size) STATIC_MAGIC_ATTACKS(orthogonal, pos, value, bit_size)

#define ASSIGN_MAGIC_VALUE(type, pos)                                           \
g_##type##_magic_data[pos].value = s_##type##_value_##pos;                      \
g_##type##_magic_data[pos].shift_quantity = s_##type##_shift_quantity_##pos;    \
g_##type##_magic_data[pos].legal_moves = s_##type##_attacks_##pos;

#define ASSIGN_DIAG_MAGIC_VALUE(pos) ASSIGN_MAGIC_VALUE(diagonal, pos)
#define ASSIGN_ORTHO_MAGIC_VALUE(pos) ASSIGN_MAGIC_VALUE(orthogonal, pos)

